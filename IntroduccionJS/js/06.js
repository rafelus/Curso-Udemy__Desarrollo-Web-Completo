// Objeto Math

let resultado;

resultado = Math.PI;
console.log(resultado);
resultado = Math.round(resultado);
console.log(resultado);
resultado = Math.ceil(2.1); // ceil siempre redondea hacia arriba
console.log(resultado);
resultado = Math.floor(2.9); // floor siempre redondea hacia abajo
console.log(resultado);
resultado = Math.sqrt(144); // sqrt saca la raiz cuadrada
console.log(resultado);
resultado = Math.pow(3, 2); // pow hace la potencia (base, exp)
console.log(resultado);
resultado = Math.abs(-234); // abs convierte en absoluto el numero
console.log(resultado);
resultado = Math.min(12,34,45,56,67,8,7,89,67,45,53); // min coge el minimo de una serie de numeros
console.log(resultado);
resultado = Math.max(12,34,45,56,67,8,7,89,67,45,53); // max coge el maximo de una serie de numeros
console.log(resultado);
resultado = Math.floor(Math.random()*100); // de esta forma con random y floor se puede generar un numero aleatorio entre 0 y 99
console.log(resultado);